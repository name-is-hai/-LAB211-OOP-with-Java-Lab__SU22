public class Pig {
    double weight;
    double gSpace;

    public Pig(double weight, double gSpace) {
        this.weight = weight;
        this.gSpace = gSpace;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getgSpace() {
        return gSpace;
    }

    public void setgSpace(double gSpace) {
        this.gSpace = gSpace;
    }
}
