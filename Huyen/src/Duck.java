public class Duck {
    double weight;
    double gSapce;
    double wSapce;
    double aSpace;

    public Duck(double weight, double gSapce, double wSapce, double aSpace) {
        this.weight = weight;
        this.gSapce = gSapce;
        this.wSapce = wSapce;
        this.aSpace = aSpace;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getgSapce() {
        return gSapce;
    }

    public void setgSapce(double gSapce) {
        this.gSapce = gSapce;
    }

    public double getwSapce() {
        return wSapce;
    }

    public void setwSapce(double wSapce) {
        this.wSapce = wSapce;
    }

    public double getaSpace() {
        return aSpace;
    }

    public void setaSpace(double aSpace) {
        this.aSpace = aSpace;
    }
}
